<p>
Light Box was a project done when I worked in the communication office in College of Design. The task was to make a minute long commercial to introduce different departments and disciplines inside of the college. In the beginning, I had a hard time to generate a good concept for this commercial; I felt my thoughts were trapped in a small box. Until one day, my boss showed me a title sequence of a food TV program. They captured different cooking process with a straight look down, uncut shot in that title sequence. That video totally opened my mind, why not use the same method to introduce our different college departments? 
</p>
<p>
After that point, this project became a very interesting one, and I started running between different departments to collect representative objects and students works to use as my footage. This process also gave me the opportunity to know some new friends from other departments; they were all very supportive and interested in my project. Before the shooting process, I had to choose between shooting on top of a green screen or a light box. I did some experiments with both methods and compared the out come. Due to the inevitable shadow problem the green screen gave me, I finally chose to use a light box for the shooting.
</p>
<p>
During the shooting session, I mounted the camera onto a crane to get the straight look down shot I wanted. I also invited some of my friends to be the hand model here, but none of their hand movements matched the motion I wanted. So I had to be the hand model myself and drove the camera simultaneously, which was very tricky and funny. All the shooting was done on top of the light box, I worked as the director, the designer, the camera man, also the actor in this project. It was definitely a challenging process to a one person team, but at the same time, it was also a very interesting and fun process; and I felt all the stress and efforts were worthwhile when I watched the final out come.
</p>
