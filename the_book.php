<p>
Days in the Advanced Media Lab at NC State are productive and filled with interesting stories. We had many opportunities to work on various interesting projects and meet different clients from other industries.
</p>
<p>
This project was from a very special nonprofit organization called Hidden Voices. They gave us a series of audio recordings of the interviews of many homeless people and families. Among those recordings, the story about a young Mexican girl's family caught my attention immediately. After listened to that voice over, the initial story boards already popped up in my head. I was so touched the the concept of flipping book just came into my mind naturally. With all these preliminary thoughts, I sketched out the story boards and used the 2D motion tracking function in After Effects to map my story boards to a live video footage of flipping book. This was the first time I tried to composite the 2D animation with a live video footage. I had a lot of fun learning how to properly set up the motion tracker and how to shoot a useful video footage for later compositing. The purpose of using this technology was to maximally show a girl's innocence and childish imagination in the reality and depict a visually appealing but mentally disturbing contrast for the audience. Hence, while the animation shows each page of this girl's story, it also amplifies the misfortune of this young girl's family and triggers the audience's compassion to the highest level.
</p>
<p>
This project has been shown by the Chapel Hill Public Arts Office in June 2010. I am very glad that this helped to give the public a visual form to an often difficult and misunderstood subject.
</p>
