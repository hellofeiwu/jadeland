<h5>Innovative features:</h5>
<p>
● A great supplement for current GPS: Navigate multiple objects simultaneously according to their location and translate speed. 
</br>
● Achieve small range location-based group activities(e.g. same school campus events, shopping mall promotions, fitness center promotions)
</p>

<h5>Scenario simulation:</h5>
<p>
<strong>User:</strong> Jack, Tom</br>
<strong>Location:</strong> A Shopping Mall</br>
<strong>Time:</strong> A Saturday</br>
<strong>App:</strong> Meet</br>
</p>
<p>
Jack and Tom are friends. They are going to meet at the new shopping mall in downtown this Saturday. On Saturday morning, Tom called Jack and asked how to get to the shopping mall. Jack didn't know either, but he took out his smart phone, launched "Meet" App, and added Tom as a friend from his contact list. Tom got the friend request, also installed "Meet" on his phone. Then, Jack found the address of that shopping mall and sent it to Tom as a "Meeting" task via this App. After receipt of the address, the App gave Tom the route options immediately. When Tom began to depart, the App sent Jack a message: "Tom just departed, will arrive in 20 min." When Jack departed, the App also sent Jack a message: "Jack just departed, will arrive in 30 min."
</p>
<p>
After 20 minutes, Tom arrived at that mall first. "Meet" App switched to the "Meeting" mode and showed Tom that Jack will arrive in 10 minutes. Jack's App also showed Jack that Tom had arrived. The App gave Tom some suggested resting places in that shopping mall (such as bookstores, cafe, etc.). After 10 minutes, Jack's avatar showed up on the map, and was moving towards the shopping mall. Jack's App also switched to the "Meeting" mode after his arrival. According to Jack and Tom's location and moving speed, the App gave them three options to select a meeting point: 
</p>
<p>
A. Select your current location</br>
B. Select App suggested position</br>
C. Custom Location 
</p>
<p>
After the discussion, Tom selected a Nike Store as their meeting point from the App suggested locations. Jack agreed Tom's choice. Then the App began to navigate them to the Nike Store. Soon, Jack and Tom found each other at the Nike Store; at the same time, the App also showed "Meeting succeed!"
</p>
