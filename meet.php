<h4>LBS App UX Design</h4>
<h5>Problem analysis:</h5>
<p>
The current mainstream GPS UX design has been very sophisticated, but most of us still would encounter such a situation: Meeting people at an unfamiliar, general or complex location, e.g. shopping malls, parks. We have to spend a lot of energy to find our friend after we both arrive. Usually, we will call each other and let the other side know our location. But in most of cases, neither side know their exact location and it is hard to describe an unfamiliar location to other people. Hence, this kind of unclear communication is not very efficient. Sometimes, it can even create confusions and arguments.
</p>
<p>
In this case, I think, letting both parties know each other's location, ensuring the smooth communication, finding a specific meeting point are the three keys to the solution. After these three preconditions got tackled we can let the GPS do the rest to navigate both parties to the same meeting point. Naturally, finding and meeting someone will become easy and pleasant.
</p>
